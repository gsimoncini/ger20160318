// Clase base que contiene las operaciones 
// de almacenaje, direcciona a las correspondientes 
// clases en la capa de negocios
public abstract class DataObject
{

	public DataObject()
	{
		
	}

	// Envia el comprobante instanciado
	// en memoria a almacenarse 
	// en la base de datos
	public virtual void Commit()
	{
		
	}

	// Elimina el comprobante instanciado
	// en la clase y lo inicializa para
	// comenzar desde cero
	public virtual void Delete()
	{
		
	}

	public virtual void Flush()
	{

	}



}// END CLASS DEFINITION DataObject

