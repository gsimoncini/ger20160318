		private void chkListasDePrecios_ItemCheck(object sender, ItemCheckEventArgs e)
		{
			int count = 1;
			for(int i = 0; i<chkListasDePrecios.Items.Count; i++)
			{
				mz.erp.businessrules.ConsultarListasDePrecios.ListaDePrecioView lp = (mz.erp.businessrules.ConsultarListasDePrecios.ListaDePrecioView)chkListasDePrecios.Items[i];
				if(((i == e.Index && e.NewValue.CompareTo(System.Windows.Forms.CheckState.Checked) == 0) || (i != e.Index && chkListasDePrecios.GetItemChecked(i))) && count < 11)
				{
					Janus.Windows.GridEX.GridEXColumnCollection col = gridResultado.RootTable.Columns;
					col["PrecioLista" + count.ToString()].Caption = lp.Descripcion;	
					count++;
				}				
			}
			if(count < 11)
			{
				for(int i = count; i<11; i++)
				{
					Janus.Windows.GridEX.GridEXColumnCollection col = gridResultado.RootTable.Columns;
					col["PrecioLista" + i.ToString()].Caption = "P. Lista " + i.ToString();	
				}
			}
		}


		private void btnBajar_Click(object sender, EventArgs e)
		{
			int index = chkListasDePrecios.SelectedIndex;
			if(index != -1)
			{
				if(index < chkListasDePrecios.Items.Count - 1)
				{
					bool chkIndex = chkListasDePrecios.GetItemChecked(index);
					bool chkIndex1 = chkListasDePrecios.GetItemChecked(index +1);
					object aux = chkListasDePrecios.Items[index];
					chkListasDePrecios.Items[index] = chkListasDePrecios.Items[index + 1];
					chkListasDePrecios.SetItemChecked(index, chkIndex1);
					chkListasDePrecios.Items[index + 1] = aux;
					chkListasDePrecios.SetItemChecked(index +1, chkIndex);
					chkListasDePrecios.SelectedIndex = index + 1;
					setListasDePreciosDescriptions();
				}
			}

		
		}

		private void btnSubir_Click(object sender, EventArgs e)
		{
			
			int index = chkListasDePrecios.SelectedIndex;
			if(index != -1)
			{
				if(index > 0)
				{
					bool chkIndex = chkListasDePrecios.GetItemChecked(index);
					bool chkIndex1 = chkListasDePrecios.GetItemChecked(index -1);
					object aux = chkListasDePrecios.Items[index];
					chkListasDePrecios.Items[index] = chkListasDePrecios.Items[index - 1];
					chkListasDePrecios.SetItemChecked(index, chkIndex1);
					chkListasDePrecios.Items[index - 1] = aux;
					chkListasDePrecios.SetItemChecked(index -1, chkIndex);
					chkListasDePrecios.SelectedIndex = index -1;
					setListasDePreciosDescriptions();
				}
			}
		}

		private void gridResultado_FormattingRow(object sender, Janus.Windows.GridEX.RowLoadEventArgs e)
		{
			if (e.Row.RowType == Janus.Windows.GridEX.RowType.Record)
			{
				foreach(Janus.Windows.GridEX.GridEXFormatCondition cnd in gridResultado.RootTable.FormatConditions)
				{
					if(cnd.Value1.GetType().Name.Equals("GridEXColumn"))
					{		
						Janus.Windows.GridEX.GridEXColumn col = (Janus.Windows.GridEX.GridEXColumn)cnd.Value1; 
						decimal valor1 = (decimal)e.Row.Cells[cnd.Column.Key].Value;
						decimal valor2 = (decimal)e.Row.Cells[col.Key].Value;
						if (valor1 > valor2)
							e.Row.RowStyle = cnd.FormatStyle;
					
					}
				}
			}
		}


