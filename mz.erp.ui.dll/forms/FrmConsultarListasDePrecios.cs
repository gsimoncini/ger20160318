		private void RefreshData()
		{
			Util.MoveFocus(this.ContainerJerarquias);
			this.Cursor = Cursors.WaitCursor;
			_uiController.ListasDePreciosSeleccionadas = new ArrayList();
			if(chkListasDePrecios.CheckedItems.Count > 0)
			{
				ArrayList listasSeleccionadas = new ArrayList();
				listasSeleccionadas.AddRange(chkListasDePrecios.CheckedItems);
				_uiController.ListasDePreciosSeleccionadas = listasSeleccionadas;
			}
			_uiController.RefreshData();
			this.Cursor = Cursors.Default;
		}

		private void RefreshData(object sender, EventArgs e)
		{			
			gridResultado.SetDataBinding(null, null);
			gridResultado.SetDataBinding(_uiController.Result, null);
			if (gridManagerView1.AllowGroupCollapse)
				this.gridResultado.CollapseGroups();
			else
				if (gridResultado.RootTable.Groups.Count > 0 )
				gridResultado.RootTable.Groups[0].Expand();
			if (gridManagerView1.AllowExportExcel || gridManagerView1.AllowSortByAgregateColumn)
				gridManagerView1.SetTable(_uiController.Result);			
		}

		private void ResetFilters()
		{
			mzProductosControl1.ClearControl();
			_uiController.Codigo=string.Empty;

            chkActivo.Checked=false;
			hierarchicalSearchControl1.ClearSelectedNodes();
			for(int i = 0; i<chkListasDePrecios.Items.Count; i++)
				chkListasDePrecios.SetItemChecked(i, false);
			rbPesos.Checked=true;

			gridResultado.SetDataBinding(null, null);
            gridResultado.Refresh();
		}

		private void setListasDePreciosDescriptions()
		{
			int count = 1;
			for(int i = 0; i<chkListasDePrecios.Items.Count; i++)
			{
				mz.erp.businessrules.ConsultarListasDePrecios.ListaDePrecioView lp = (mz.erp.businessrules.ConsultarListasDePrecios.ListaDePrecioView)chkListasDePrecios.Items[i];
				if(chkListasDePrecios.GetItemChecked(i) && count < 11)
				{
					Janus.Windows.GridEX.GridEXColumnCollection col = gridResultado.RootTable.Columns;
					col["PrecioLista" + count.ToString()].Caption = lp.Descripcion;	
					count++;
				}				
			}
			if(count < 11)
			{
				for(int i = count; i<11; i++)
				{
					Janus.Windows.GridEX.GridEXColumnCollection col = gridResultado.RootTable.Columns;
					col["PrecioLista" + i.ToString()].Caption = "P. Lista " + i.ToString();	
				}
			}

		}
