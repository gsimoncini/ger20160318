		private void DumpControls()
		{
			_uiController.FechaDesde=Convert.ToDateTime(this.FechaDesde.Value);
			_uiController.FechaHasta=Convert.ToDateTime(this.FechaHasta.Value);
			_uiController.FilePath=this.txtFileName.Text;
		}

		private void btnFileChooser_Click(object sender, System.EventArgs e)
		{
			SaveFileDialog saveFileDialog1 = new SaveFileDialog();
			saveFileDialog1.Filter = "txt files (*.txt)|*.txt"  ;
			saveFileDialog1.RestoreDirectory = true ;
			if(saveFileDialog1.ShowDialog() == DialogResult.OK)
			{
				txtFileName.Text = saveFileDialog1.FileName;
			}
		}

		private void toolBarStandar_ButtonClick(object sender, System.Windows.Forms.ToolBarButtonClickEventArgs e)
		{
			switch ( toolBarStandar.Buttons.IndexOf( e.Button ) )
			{
				case 0:
					this.Cursor = System.Windows.Forms.Cursors.WaitCursor;
					DumpControls();
					if (_uiController.ExportarDatos())					
					{
						this.Cursor = System.Windows.Forms.Cursors.Default;
						MessageBox.Show(this, "Datos exportados con éxito!", "Exito!", System.Windows.Forms.MessageBoxButtons.OK, System.Windows.Forms.MessageBoxIcon.Information );
					}
					else
					{
						this.Cursor = System.Windows.Forms.Cursors.Default;
						MessageBox.Show(this, "No se pudieron exportar los datos", "Atención!", System.Windows.Forms.MessageBoxButtons.OK, System.Windows.Forms.MessageBoxIcon.Warning );
					}
                    this.Close();
					break;
				case 2:
					CloseForm();
					break;
			}
		
		}

		
		public void CloseForm()
		{			
			if (_uiController.PreguntarAlCancelar)
			{
				if ((MessageBox.Show(this,"Está a punto de cerrar el fomulario. Realmente desea realizar esta operación?","Atención!",System.Windows.Forms.MessageBoxButtons.OKCancel, System.Windows.Forms.MessageBoxIcon.Stop )== System.Windows.Forms.DialogResult.OK))
					Close();
			}
			else
			{
				Close();
			}
		}

