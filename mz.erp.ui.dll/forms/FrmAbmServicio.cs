		private void SaveChanges()
		{	
			Util.MoveFocus(this.ultraExplorerBarContainerControl2);
			this.Cursor = Cursors.WaitCursor;
			if(_uiController.NombreServicio.Equals(string.Empty))
				MessageBox.Show("Debe ingresar un nombre para el servicio","Advertencia",System.Windows.Forms.MessageBoxButtons.OK);
			else if(_uiController.ExisteNombreServicio()) 
				MessageBox.Show("El nombre del servicio ingresado ya existe, por favor elija otro","Advertencia",System.Windows.Forms.MessageBoxButtons.OK);
            //German 20100826 - tarea 818
            else
                if (!_uiController.ExisteAlMenosUnaTarea())
                    MessageBox.Show("El servicio debe tener asociado al menos una tarea", "Error", System.Windows.Forms.MessageBoxButtons.OK);
            else
               if (!_uiController.TotalServicioIgualTotalTareas())
                        MessageBox.Show("El Costo del Servicio debe ser igual a la suma de los precios de las tareas", "Error", System.Windows.Forms.MessageBoxButtons.OK);
           //Fin German 20100826 - tarea 818    
            else
			{
				_uiController.SaveChanges();				
				btnAceptar.Enabled = false;
				btnCancelar.Enabled = false;
				btnAgregarServicio.Enabled = true;
				if(!_uiController.NombreServicio.Equals(string.Empty))
				{
					btnModificarServicio.Enabled = true;
					//btnBorrarServicio.Enabled = true;
				}
				else
				{
					btnModificarServicio.Enabled = false;
					btnBorrarServicio.Enabled = false;
				}
				panelBotonesABMServicios.Visible = true;
				panelBotonesConfirmacionSevicio.Visible = false;
				mzCEServicios.Enabled = true;
				panelBotonesABMServicios.Enabled = true;
				panelABMServicios.Enabled = false;
				chkTareas.Enabled = false;
			}
			this.Cursor = Cursors.Default;

		}


