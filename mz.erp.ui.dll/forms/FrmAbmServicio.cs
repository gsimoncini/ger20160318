		private void _uiController_TareasHasChanged(object sender, EventArgs e)
		{	
			_processItem = false;
			for(int i = 0; i < chkTareas.Items.Count; i++)
				chkTareas.SetItemChecked(i, false);
			foreach(businessrules.Tarea tarea in _uiController.TareasServicioSeleccionado)
			{
				int index = chkTareas.Items.IndexOf(tarea);
				chkTareas.SetItemChecked(index, true);
			}
			_processItem = true;
			txtNombreServicio.Text = _uiController.NombreServicio;			
			uneDuracionDias.Value = _uiController.DuracionDiasServicio;
			uneCosto.Value = _uiController.CostoServicio;
			uneCostoMaximo.Value = _uiController.CostoMaximoServicio;
			uneCostoPresupuesto.Value = _uiController.CostoPresupuestoServicio;
		}

