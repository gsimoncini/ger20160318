		private void SaveChanges()
		{	
			Util.MoveFocus(this.ultraExplorerBarContainerControl2);
			this.Cursor = Cursors.WaitCursor;
			_uiController.Descripcion = txtDescripcion.Text;		
			_uiController.Codigo = txtCodigo.Text;			
			_uiController.IdProducto = mzProductosControl.GetIdProducto();
			_uiController.Complejidad = cboComplejidad.Text;			
			_uiController.Duracion = Convert.ToDecimal(uneDuracion.Value);
			_uiController.ValorTecnico = Convert.ToDecimal(uneValorTecnico.Value);
			_uiController.ValorCliente = Convert.ToDecimal(uneValorCliente.Value);
			_uiController.ValorTecnicoProvisorio = Convert.ToDecimal(uneValorTecnicoProvisorio.Value);
			if(_uiController.Descripcion.Equals(string.Empty))
				MessageBox.Show("Debe ingresar un nombre para la tarea","Advertencia",System.Windows.Forms.MessageBoxButtons.OK);
			else if(_uiController.ExisteNombreTarea()) 
				MessageBox.Show("El nombre de la tarea ingresada ya existe, por favor elija otro","Advertencia",System.Windows.Forms.MessageBoxButtons.OK);
			else
			{
				_uiController.SaveChanges();		
				btnAgregarTarea.Enabled = true;
				if(!_uiController.Descripcion.Equals(string.Empty))
				{
					btnModificarTarea.Enabled = true;
					//btnBorrarTarea.Enabled = true;
				}
				else
				{
					btnModificarTarea.Enabled = false;
					btnBorrarTarea.Enabled = false;
				}
				panelBotonesABMTareas.Visible = true;
				panelBotonesConfirmacionTarea.Visible = false;
				mzCETareas.Enabled = true;
				panelBotonesABMTareas.Enabled = true;
				panelABMTareas.Enabled = false;
				btnAceptar.Enabled = false;
				btnCancelar.Enabled = false;
			}
			this.Cursor = Cursors.Default;

		}

